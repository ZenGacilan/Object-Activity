// Create a new JavaScript file named objectActivity.js

// Task 1: Create an object called car
const car = {
    type: "Honda",
    model: "Civic",
    color: "Red"
};

// Task 2: Use typeof to check the type of the object
console.log("Type of car:", typeof car);

// Task 3: Update the type property
car.type = "Toyota";
console.log("Updated car object:", car);

// Task 4: Add a new property
car.wheels = 4;
console.log("Car object with wheels property:", car);
